            <p>Between the 12th and 14th centuries, Ile-Ife was the centre of the Yoruba world. It was an emporium and holy city. Its economy was based on a novel technology of glass manufacture mainly devoted to making beads, the primary currency of power, authority and wealth in the region.</p>
            <p>Ile-Ife remains the only place in sub-Saharan Africa known as an industrial centre for primary glass production.</p>
            <p>Ile-Ife used its technological and economic advantages to restructure the ideology of divine kingship. It also used this advantage to standardise the Yoruba religious system (Orisa pantheon) and make itself (literally) the beginning and end of time. It brought vast territories, as far as the River Niger and the Atlantic coast, under its political control and cultural influence. These included even non-Yoruba-speaking peoples.</p>
